public class SquarePattern {
    public static void main(String args[]){
        System.out.print("pattern!: square\n");
        for(int j = 0 ; j < 9 ; j++ ){
            for(int i = 0 ; i < 9 ; i++ ){
                System.out.print(" * ");
            }
            System.out.println("");
        }
    }
}